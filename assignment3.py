# 1. upper() – Converts string to uppercase
text = "hello world"
print(text.upper())  

# 2. lower() – Converts string to lowercase
text = "HELLO WORLD"
print(text.lower()) 

# 3. strip() – Removes leading and trailing spaces
text = "   Python   "
print(text.strip())  

# 4. replace() – Replaces a substring with another
text = "I love Java"
print(text.replace("Java", "Python"))  

# 5. split() – Splits a string into a list
text = "apple,banana,grape"
print(text.split(","))  

# 6. join() – Joins elements of a list into a string
words = ["Python", "is", "awesome"]
print(" ".join(words))  

# 7. find() – Finds the index of a substring
text = "Welcome to Python"
print(text.find("Python"))  

# 8. count() – Counts occurrences of a substring
text = "banana"
print(text.count("a"))  

# 9. startswith() – Checks if a string starts with a given substring
text = "Python is great"
print(text.startswith("Python"))  

# 10. endswith() – Checks if a string ends with a given substring
text = "hello.txt"
print(text.endswith(".txt"))  

# 11. capitalize() – Capitalizes the first letter
text = "python is fun"
print(text.capitalize())  # Output: "Python is fun"




